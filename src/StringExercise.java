import java.util.Scanner;

public class StringExercise {
    public static void main(String[] args) {
        String noEd = "We don't need no education";
        String thoughtCtrl = "We don't need no though control";
        System.out.println(noEd);
        System.out.println(thoughtCtrl);
        String checkIt = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(checkIt);
        String backSlash = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(backSlash);
    }
}
